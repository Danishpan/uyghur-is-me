/* ═══════════════════════════════════════════════════════
   Уйғур SuperApp — Data Encoder
   Run: node scripts/encode-data.js

   Reads raw data objects defined in this file, XOR-obfuscates
   them with a cycling key, base64-encodes the result, and
   writes self-contained JS files to data/.

   To add new words/puzzles:
     1. Edit the RAW_* objects below.
     2. Re-run: node scripts/encode-data.js
     3. No game code changes needed.

   Key: UTF-8 bytes of 'уйғур' cycling over the JSON bytes.
   ═══════════════════════════════════════════════════════ */

const fs   = require('fs');
const path = require('path');

// ── XOR encoder ──────────────────────────────────────────
const KEY_STR = 'уйғур';

function getKeyBytes(str) {
  const out = [];
  for (const ch of str) {
    const cp = ch.codePointAt(0);
    if      (cp < 0x80)   out.push(cp);
    else if (cp < 0x800)  out.push(0xC0 | (cp >> 6), 0x80 | (cp & 0x3F));
    else                  out.push(0xE0 | (cp >> 12), 0x80 | ((cp >> 6) & 0x3F), 0x80 | (cp & 0x3F));
  }
  return out;
}

function encode(data) {
  const json      = JSON.stringify(data);
  const jsonBytes = Buffer.from(json, 'utf8');
  const keyBytes  = getKeyBytes(KEY_STR);
  const out       = Buffer.alloc(jsonBytes.length);
  for (let i = 0; i < jsonBytes.length; i++) {
    out[i] = jsonBytes[i] ^ keyBytes[i % keyBytes.length];
  }
  return out.toString('base64');
}

function writeDataFile(filename, globalVar, data, comment) {
  const b64     = encode(data);
  const outPath = path.join(__dirname, '..', 'data', filename);
  const content =
`/* ══════════════════════════════════════════════════════
   ${comment}
   GENERATED — edit scripts/encode-data.js, then re-run:
     node scripts/encode-data.js
   Do not edit this file directly.
   ══════════════════════════════════════════════════════ */
window.__GAME_DATA__=window.__GAME_DATA__||{};
window.__GAME_DATA__['${globalVar}']="${b64}";
`;
  fs.writeFileSync(outPath, content, 'utf8');
  console.log(`✅  Wrote data/${filename}  (${jsonBytes(data)} chars JSON → ${b64.length} chars b64)`);
}

function jsonBytes(data) { return Buffer.from(JSON.stringify(data), 'utf8').length; }


